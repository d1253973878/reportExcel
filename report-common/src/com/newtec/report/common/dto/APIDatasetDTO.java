package com.newtec.report.common.dto;

import java.util.List;

import com.newtec.report.common.entity.api.APIDataset;
import com.newtec.report.common.entity.api.APIDatasetField;
import com.newtec.report.common.entity.api.APIDatasetParam;

/**
 * API数据集数据传输 DTO
 * @author 黄腾
 * @Description 
 * @date 2020年12月24日 下午1:55:49
 */
public class APIDatasetDTO {

	/**
	 * API 数据集 信息对象
	 */
	APIDataset dataset;
	
	/**
	 * 动态参数
	 */
	List<APIDatasetParam> params;
	
	/**
	 * 动态报表配置明细数组
	 */
	List<APIDatasetField> fields;

	public APIDataset getDataset() {
		return dataset;
	}

	public void setDataset(APIDataset dataset) {
		this.dataset = dataset;
	}

	public List<APIDatasetParam> getParams() {
		return params;
	}

	public void setParams(List<APIDatasetParam> params) {
		this.params = params;
	}

	public List<APIDatasetField> getFields() {
		return fields;
	}

	public void setFields(List<APIDatasetField> fields) {
		this.fields = fields;
	}
	
}
